package it.tecnet.crs.componenti.web.bean;


public class EnteEmittente extends TipoNormativaCommon{
	
	private long idEnteEmittente;
	
	private String codice;
	
	private String descrizione;
	
	

	public long getIdEnteEmittente() {
		return idEnteEmittente;
	}

	public void setIdEnteEmittente(long idEnteEmittente) {
		this.idEnteEmittente = idEnteEmittente;
	}

	public String getCodice() {
		return codice;
	}

	public void setCodice(String codice) {
		this.codice = codice;
	}

	public String getDescrizione() {
		return descrizione;
	}

	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}
	


	
	
	


}
