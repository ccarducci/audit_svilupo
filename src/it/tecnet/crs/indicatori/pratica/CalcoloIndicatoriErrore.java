package it.tecnet.crs.indicatori.pratica;

public class CalcoloIndicatoriErrore {
	private String tipoRischio;
	private String tipoErrore;
	private String messaggio;

	public String getTipoRischio() {
		return tipoRischio;
	}

	public void setTipoRischio(String tipoRischio) {
		this.tipoRischio = tipoRischio;
	}

	public String getTipoErrore() {
		return tipoErrore;
	}

	public void setTipoErrore(String tipoErrore) {
		this.tipoErrore = tipoErrore;
	}

	public String getMessaggio() {
		return messaggio;
	}

	public void setMessaggio(String messaggio) {
		this.messaggio = messaggio;
	}
}
